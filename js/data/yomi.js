const isKana = (ch) => /[\u3041-\u3096\u30a1-\u30f6ー]/.test(ch);
const kataToHira = (s) => s.replace(/[\u30a1-\u30f6]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0x60));

/** 文字列を「読み(ひらがな)」に寄せる。辞書優先→単漢字→かなはそのまま。 */
export function toYomi(input) {
  let s = String(input || "").trim().toLowerCase().replace(/[\s　]/g, "");
  if (!s) return "";
  // 1) 語まるごと辞書ヒット
  if (WORD_YOMI[s]) return kataToHira(WORD_YOMI[s]);
  // 2) 1文字ずつ: かなはそのまま、漢字は単漢字辞書、無ければ原字を残す
  let out = "";
  for (const ch of s) {
    if (isKana(ch)) out += kataToHira(ch);
    else if (KANJI_YOMI[ch]) out += KANJI_YOMI[ch];
    else out += ch;   // 変換できない漢字はそのまま(表記一致でのみマッチ)
  }
  return out;
}
